export async function signInWithEmailAndPassword(
  email: string,
  password: string,
  redirectTo?: string,
) {
  await firebase.auth().signInWithEmailAndPassword(email, password);

  const router = useRouter();
  router.push(redirectTo ?? '/account');
}
